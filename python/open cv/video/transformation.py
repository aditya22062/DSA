import cv2 as cv
import numpy as np

img=cv.imread('photo/park.jpg')

#translation means shifting rotationg etc..
def translation(img,x,y):
    transMat=np.float32([[1,0,x],[0,1,y]])
    dimensions=(img.shape[1],img.shape[0])
    return cv.warpAffine(img,transMat,dimensions)

#--> -x menans left shift
#-->+x means Right
#-->-y means up
#-->+y means down
translat=translation(img,100,100)
cv.imshow('Translation',translat)
















































































































































